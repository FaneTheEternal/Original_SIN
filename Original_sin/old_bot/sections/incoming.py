    def execute(self, vk, user, text):
        profile, created = IncomingProfile.objects.get_or_create(
            user=user
        )
        message = ''
        attachment = None
        keyboard = None

        admission = Admission()

        if profile.status == IncomingProfile.ADMISSION:
            return admission.execute(vk, user, text)

        elif text == self.ADMISSION:
            profile.status = IncomingProfile.ADMISSION
            profile.save()
            message = self.TEXTS.get(text, '-')
            keyboard = admission.keyboard()

        elif text in self.TEXT_ONLY:
            message = self.TEXTS.get(text, '-')
            attachment = self.ATTACHMENTS.get(text, None)
            keyboard = self.keyboard()

        elif text == self.BACK:
            from ..chat_bot import go_home
            profile.set_default(full=True)
            return go_home(vk, user)

        else:
            from ..chat_bot import sleep
            profile.set_default(full=True)
            return sleep(vk, user)

        try:
            vk.messages.send(
                peer_id=str(user.user_id),
                message=message,
                random_id=get_random_id(),
                keyboard=keyboard.get_keyboard(),
                attachment=attachment
            )
        except ApiError as pezdos:
            print('User_id: ', user.user_id, file=sys.stderr)
            print('Message: ', message, file=sys.stderr)
            print('Attachment: ', attachment, file=sys.stderr)
            print('Pezdos: ', pezdos, file=sys.stderr)

    def keyboard(self, keyboard=None, one_time=True):
        if keyboard is None:
            keyboard = VkKeyboard(one_time=one_time)

        NEW_LINE = [i for i in range(len(self.WORDS)) if i % 2 == 1]
        for i in range(len(self.WORDS)):
            keyboard.add_button(
                self.WORDS[i],
                color=VkKeyboardColor.DEFAULT
            )
            if i in NEW_LINE:
                keyboard.add_line()

        keyboard.add_button(
            self.BACK,
            color=VkKeyboardColor.NEGATIVE
        )

        return keyboard

